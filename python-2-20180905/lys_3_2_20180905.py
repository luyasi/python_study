# python文件名命名方式
# 驼峰命名
# 大驼峰
# FirstName
# 小驼峰
# firstName

# 下划线命名
# first_name

# python文件名不要使用关键字
import keyword

print(keyword.kwlist)

# python文件名命名要求
# 1.使用中文不会报错，但是禁止使用中文。
# 2.可以使用数字，但是不要全部使用数字。
# 3.可以使用-和_等部分分隔符分割文件名。
# 4.文件名要有描述性。
# 5.文件名不能有关键字。

# python后缀名
# .py    常用的python代码
# .pyc   临时文件
# .pyw   纯图形化界面的程序文件。
